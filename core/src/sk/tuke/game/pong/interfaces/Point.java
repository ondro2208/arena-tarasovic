package sk.tuke.game.pong.interfaces;

/**
 * Point interface uses for getting information about point.
 */
public interface Point {
	/**
	 * Get x position of point
	 *
	 * @return x position
	 */
	float getPointX();

	/**
	 * Get y position of point
	 * @return y position
	 */
	float getPointY();
}
